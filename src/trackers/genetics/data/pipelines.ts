/** Локализованная подпись (русский основной, английский — для тултипа). */
export interface L10n {
  ru: string
  en: string
}

/** Шаг пайплайна: машина + входы (ресурсы) + продукты. */
export interface PipelineStep {
  machine: L10n
  /** Входные ресурсы/предметы. */
  inputs: L10n[]
  /** Основной продукт шага. */
  outMain: string
  /** Побочный продукт / оговорка (необязательно). */
  outNote?: string
  /** Развёрнутая справка по машине (что делает, тонкости). */
  guide: string
}

/** Вспомогательная машина тулчейна (вне основной цепочки). */
export interface AuxMachine {
  machine: L10n
  note: string
}

/** Тулчейн (набор машин одного мода). */
export interface Pipeline {
  id: 'binnie' | 'gendustry'
  label: string
  steps: PipelineStep[]
  aux: AuxMachine[]
}

