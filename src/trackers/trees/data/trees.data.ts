/** Обогащает сырые деревья плодом и стоимостью посадки. */
function buildTrees(seeds: readonly TreeSeed[]): Tree[] {
  return seeds.map((t) => ({
    ...t,
    fruit: FRUITS[t.id],
    plant: PLANT[t.id] ?? 1,
  }))
}

/** Полный список деревьев (обогащённый). */
export const TREES: readonly Tree[] = buildTrees(TREE_SEEDS)

/** Индекс по id для O(1) доступа. */
export const BY_ID: Readonly<Record<string, Tree>> = Object.fromEntries(TREES.map((t) => [t.id, t]))
